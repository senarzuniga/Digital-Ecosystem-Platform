"""
AI Agent Orchestration Service.

Implements:
  - Agent registry and lifecycle
  - Planner: decide which agent should handle an event
  - Executor: run an agent action
  - Short-term (in-memory) + long-term (DB) memory
  - Multi-agent coordination via event bus
"""

from __future__ import annotations

import logging
import uuid
from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Any, Callable, Coroutine, Dict, List, Optional

from sqlalchemy.ext.asyncio import AsyncSession

from backend.core.events import Event, Topics, get_event_bus
from backend.models.alert import AlertCategory, AlertCreate, AlertSeverity
